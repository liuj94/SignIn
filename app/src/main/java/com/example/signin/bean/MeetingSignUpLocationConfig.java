package com.example.signin.bean;

public class MeetingSignUpLocationConfig {

    private int signUpLocationId;
    private int printStatus;
    private int avatarStatus;
    private int printModel;
    private int avatarModel;

    public int getSignUpLocationId() {
        return signUpLocationId;
    }

    public void setSignUpLocationId(int signUpLocationId) {
        this.signUpLocationId = signUpLocationId;
    }

    public int getPrintStatus() {
        return printStatus;
    }

    public void setPrintStatus(int printStatus) {
        this.printStatus = printStatus;
    }

    public int getAvatarStatus() {
        return avatarStatus;
    }

    public void setAvatarStatus(int avatarStatus) {
        this.avatarStatus = avatarStatus;
    }

    public int getPrintModel() {
        return printModel;
    }

    public void setPrintModel(int printModel) {
        this.printModel = printModel;
    }

    public int getAvatarModel() {
        return avatarModel;
    }

    public void setAvatarModel(int avatarModel) {
        this.avatarModel = avatarModel;
    }
}
